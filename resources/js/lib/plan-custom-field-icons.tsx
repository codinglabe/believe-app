export type PlanCustomFieldIconName = keyof typeof PLAN_CUSTOM_FIELD_ICONS

export const PLAN_CUSTOM_FIELD_ICON_MAP: Record<string, LucideIcon> =
    PLAN_CUSTOM_FIELD_ICONS as unknown as Record<string, LucideIcon>

export const PLAN_CUSTOM_FIELD_ICON_ENTRIES: { name: string; Icon: LucideIcon }[] = (
    Object.keys(PLAN_CUSTOM_FIELD_ICONS) as PlanCustomFieldIconName[]
)
    .sort((a, b) => a.localeCompare(b))
    .map((name) => ({ name, Icon: PLAN_CUSTOM_FIELD_ICONS[name] }))

export function getPlanCustomFieldLucideIcon(name: string | undefined | null): LucideIcon {
    if (!name?.trim()) return Mail
    return PLAN_CUSTOM_FIELD_ICON_MAP[name.trim()] ?? Mail
}
